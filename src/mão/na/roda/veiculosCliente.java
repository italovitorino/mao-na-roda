    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int dias = Integer.valueOf(jtDias.getText());
        String placa = jtPlaca.getText();
        
        int resposta = JOptionPane.showConfirmDialog(
                null,
                "O valor do seu aluguel ficará: R$ "+ cAluguel.simularValor(placa, dias)+" \n \n Caso tenha interesse, aperte YES para dar contiuidade com o seu atendimento!",
                "Simulação",
                JOptionPane.INFORMATION_MESSAGE);
        
        if (resposta == JOptionPane.YES_OPTION) {
            event.mudaTela(new jAgendamento(), this);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

